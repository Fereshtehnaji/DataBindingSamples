package com.naji.databindingsamples;

public class Actor {
    private int id;
    private String name;
    private String bio;
    private int imageId;

    public Actor(String name, String bio, int imageId) {
        this.name = name;
        this.bio = bio;
        this.imageId = imageId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBio() {
        return bio;
    }

    public void setBio(String bio) {
        this.bio = bio;
    }

    public int getImageId() {
        return imageId;
    }

    public void setImageId(int imageId) {
        this.imageId = imageId;
    }
}
